    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Vehiculo c =new Vehiculo();
        c.setMarca(txtMarca.getText());
        c.setModelo(txtModelo.getText());
        c.setColor(txtColor.getText());
        c.setTipoMotor(txttipoMotor.getText());
        c.setKilometraje(Integer.parseInt(txtKilometraje.getText()));
        c.setAnoFabricacion(Integer.parseInt(txtAnoFabricacion.getText()));
        txtVehiculo.append(c.toString()+"\n");
        //LIMPIADOR DE CONTROLES:
        txtMarca.setText("");
        txtModelo.setText("");
        txtColor.setText("");
        txtAnoFabricacion.setText("");
        txttipoMotor.setText("");
        txtKilometraje.setText("");
        txtMarca.setRequestFocusEnabled(true);
       
    }//GEN-LAST:event_btnGuardarActionPerformed

